#this is a car simulation game in which if the user types starts then the cars starts, if we type stop the car will stop and if we type quit the game will exit 
input = input("type 'start' to start the game! ")
if input == 'start':
    print('the game has started')
elif input == 'stop':
    print('the game has stopped')
elif input == 'pause':
    print('game paused')
    
   
